    const impact  = calculateEnvironmentalImpact(sessionTokens);
    const co2g    = (impact.co2Kg * 1000).toFixed(1);

    const m = Math.floor(elapsed / 60);
    const s = elapsed % 60;
    const timeStr = m > 0 ? `${m} min ${s} sec` : `${s} seconds`;

